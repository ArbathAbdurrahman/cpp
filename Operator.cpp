#include <iostream>
using namespace std;

int main() {
    // Penjumalahan
    int tambah =  5 + 5;
    cout << tambah << endl;

    // Pengurangan
    int kurang = 5 - 4;
    cout << kurang << endl;

    // Perkalian
    int kali = 5 * 5;
    cout << kali << endl;

    // Pembagian
    int bagi = 16 / 4;
    cout << bagi << endl;

    // Modulus (sisa bagi)
    int mod = 25 % 4;
    cout << mod << endl;

    // Increment
    int inc = ++tambah;
    cout << inc << endl;

    // Decrement
    int dec = --inc;
    cout << dec << endl;
}